                <h2>How to Avoid</h2>

                <p>All forms of hepatitis are best avoided by using condoms during all sexual contact (and never sharing an intravenous needle). Any exchange of blood or bodily fluids can spread the virus, so sharing razors, toothbrushes or anything that might have small amounts of blood on it is best avoided.</p>

                <h2>Treatment</h2>

                <p>There is often no specific treatment for acute hepatitis B. However, with pain relief for symptoms, rest, a healthy diet and no alcohol, most people tend to be symptom-free or recover completely within a couple of months. Chronic hepatitis (caused only by B and C) may be treated with antiviral medication.</p>
