                <p>Hepatitis is a viral infection that causes inflammation of the liver. There are three main types (A, B and C) that can be spread by sexual contact.</p>
                <p>Hepatitis A can be found in the faeces (poo) of a sexual partner and can be spread through anal or oral sex. It can also be picked up from contaminated food or water in countries with poor sanitation. Complications of hepatitis A are rarely serious.</p>
                <p>Hepatitis B, which can be spread through the exchange of blood and bodily fluids during unprotected sex (or through using dirty needles and contaminated blood products) is 100 times more infectious than HIV. However, many people may not even realise they have been infected with the virus, as symptoms may not develop immediately, or at all.</p>
                <p>Hepatitis C is also spread through the exchange of bodily fluids or blood products (or through sharing needles). Both hepatitis B and C can cause long-term illness and permanent damage to the liver if left untreated.</p>

