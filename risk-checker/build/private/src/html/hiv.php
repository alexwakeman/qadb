                <h2>How to Avoid</h2>

                <p>For such a potentially life-changing illness, the steps to avoid HIV are thankfully relatively straightforward. ALWAYS use condoms during ALL sexual contact (and if you're an intravenous drug user, never share a needle). Even if you're in a stable relationship, don't have unprotected (condomless) sex unless both yourself and your partner have been tested for sexually transmitted infections including HIV and been given a clean bill of health.</p>

                <h2>Treatment</h2>

                <p>A blood test, usually taken between one and three months after the risk of infection has been identified, can detect the presence of the HIV virus. Once you get infected, there is no way of getting rid of the virus from the body completely. However, modern medication means that most people with HIV can lead a relatively normal life. The earlier you get diagnosed and start the treatment, the better the outcome. If you have any concerns that you may have been infected, get along to your local GUM clinic or speak to your GP at the earliest opportunity.</p>
