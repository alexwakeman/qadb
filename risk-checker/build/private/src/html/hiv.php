                <p>HIV (Human Immunodeficiency Virus) is most commonly passed on by sexual contact. If untreated, the virus weakens the immune system and eventually the body can no longer fight infections. This stage of the infection is called AIDS, which is the fourth-biggest killer worldwide.</p>
                <p>HIV is incurable. The infection can be passed during unprotected anal, vaginal and oral sex. The virus is transferred via blood, semen, vaginal fluids and even breast milk. It can also be transmitted by sharing needles, which places intravenous drug users at risk. Although gay men who engage in unprotected sex are a high risk group, anyone who fails to practice safe sex risks infection.</p>

