                <p>Scabies is spread by close physical contact, especially during sexual contact (or in overcrowded living conditions). The only way to avoid scabies for sure is to avoid close physical or sexual contact with sufferers. If you experience any symptoms, go to see your doctor as soon as you can.</p>

